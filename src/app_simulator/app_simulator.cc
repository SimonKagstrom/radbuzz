AppSimulator::AppSimulator(ApplicationState& app_state, BleServerHost& ble_server)
    : m_application_state(app_state)
    , m_ble_server(ble_server)
    , m_current_point(*Wgs84ToOsmPoint(
          {
              // Stockholm
              59.34443143179733,
              18.04792142012441,

              // Enköping
              // 59.646331787827336, 17.07769480747484,
          },
          15))
{
    SetupStreetOrder();
}

hal::IGps&
AppSimulator::GetSimulatedGps()
{
    return m_gps;
}

void
AppSimulator::SetupStreetOrder()
{
    for (auto& street : kStreets)
    {
        m_streets.push_back(street);
    }

    std::ranges::shuffle(m_streets, m_random_engine);

    m_cached_images.clear();

    // In meters
    m_distance_left = 500;
    m_current_image = m_random_engine() % kImages.size();
}

std::optional<milliseconds>
AppSimulator::OnActivation()
{
    m_current_point.x++;
    m_current_point.y++;

    if (m_distance_left <= 0)
    {
        m_distance_left = 100 + (m_random_engine() % 90) * 10;
        m_streets.pop_back();
        m_current_image = m_random_engine() % kImages.size();
    }
    if (m_streets.empty())
    {
        SetupStreetOrder();
    }

    auto ps = m_application_state.CheckoutPartialSnapshot<AS::distance_traveled,
                                                          AS::wh_consumed,
                                                          AS::wh_regenerated,
                                                          AS::speed>();

    auto current_street = m_streets.back();

    auto nav_info = std::format(R"VOBB(nextRd={}
nextRdDesc=
distToNext={} m
totalDist={} m
eta=13:25
ete=5 min
iconHash={:08x}32
        )VOBB",
                                current_street,
                                m_distance_left,
                                5000,
                                kImages[m_current_image].key);

    m_ble_server.Inject(kChaNav, nav_info);

    if (m_cached_images.find(kImages[m_current_image].key) == m_cached_images.end())
    {
        m_ble_server.Inject(kChaNavTbtIcon, kImages[m_current_image].data);
        m_cached_images.insert(kImages[m_current_image].key);
    }

    m_distance_left -= 10;
    ps.GetWritableReference<AS::distance_traveled>() += 10;
    ps.GetWritableReference<AS::wh_consumed>() += 5;
    ps.GetWritableReference<AS::wh_regenerated>() += 1;
    auto& speed = ps.GetWritableReference<AS::speed>();

    // Also simulate the speedometer bottoming out
    constexpr auto kMaxSpeed = 70;
    if (speed != m_target_speed)
    {
        auto sign = speed < m_target_speed ? 1 : -1;
        speed = static_cast<uint8_t>(std::clamp(speed + sign, 0, kMaxSpeed));
    }
    else
    {
        m_target_speed = m_random_engine() % kMaxSpeed;
    }

    m_gps.NextPoint(m_current_point);

    return 250ms;
}


void
AppSimulator::SimulatedGps::NextPoint(const Point& point)
{
    m_current_point = point;
    m_data_semaphore.release();
}

std::optional<hal::RawGpsData>
AppSimulator::SimulatedGps::WaitForData(os::binary_semaphore& semaphore)
{
    m_data_semaphore.acquire();

    semaphore.release();

    hal::RawGpsData out;

    out.heading = 15; // TODO
    out.speed = 25;   // TODO
    out.position = OsmPointToWgs84(m_current_point, 15);

    return out;
}
