AppSimulator::AppSimulator(ApplicationState& app_state, BleInjector& ble_server)
    : m_application_state(app_state)
    , m_ble_injector(ble_server)
    , m_state_listener(m_application_state.AttachListener<AS::demo_mode>(GetSemaphore()))
    , m_state_cache(m_application_state)
    , m_bresenham(*Wgs84ToOsmPoint(kDemoPoints[0], kDefaultZoom),
                  *Wgs84ToOsmPoint(kDemoPoints[1], kDefaultZoom))
    , m_bresenham_iterator(m_bresenham.begin())
    , m_target_heading(m_bresenham.GetHeading())
    , m_heading(m_target_heading)
{
    static_assert(kDemoPoints.size() >= 2, "At least two points are required for the demo route");
    for (auto& point : kDemoPoints)
    {
        m_demo_route.push_back(*Wgs84ToOsmPoint(point, kDefaultZoom));
    }
    auto it = m_demo_route.begin();
    m_current_point = *it;
    m_next_point = ++it;

    SetupStreetOrder();
    Awake();
}

void
AppSimulator::SetupStreetOrder()
{
    for (auto& street : kStreets)
    {
        m_streets.push_back(street);
    }

    std::ranges::shuffle(m_streets, m_random_engine);

    m_cached_images.clear();

    // In meters
    m_distance_left = 500;
    m_current_image = m_random_engine() % kImages.size();
}

std::optional<milliseconds>
AppSimulator::OnActivation()
{
    auto& co = m_state_cache.Pull();

    auto demo_active = co.Get<AS::demo_mode>();
    co.OnChangedValue<AS::demo_mode>([this, &demo_active](auto old, auto now) {
        if (old == true && now == false)
        {
            // Disable navigation
            printf("Navigation deactivated\n");
            m_application_state.CheckoutReadWrite().Set<AS::navigation_active>(false);
        }
    });

    if (demo_active == false)
    {
        // Wait until demo mode is changed
        return std::nullopt;
    }

    const auto previous_distance_left = m_distance_left;

    if (m_bresenham_iterator != m_bresenham.end())
    {
        m_current_point = *m_bresenham_iterator;
        ++m_bresenham_iterator;
    }
    else
    {
        ++m_next_point;
        if (m_next_point == m_demo_route.end())
        {
            m_next_point = m_demo_route.begin();
        }

        m_bresenham = Bresenham<Point>(m_current_point, *m_next_point);
        m_bresenham_iterator = m_bresenham.begin();
        m_target_heading = m_bresenham.GetHeading();

        m_streets.pop_back();
        m_current_image = m_random_engine() % kImages.size();
        if (m_streets.empty())
        {
            SetupStreetOrder();
        }
    }
    m_distance_left = MetersBetweenPoints(m_current_point, *m_next_point);

    const auto distance_traveled_delta =
        std::max<int32_t>(0, previous_distance_left - m_distance_left);

    auto ps = m_application_state.CheckoutPartialSnapshot<AS::distance_traveled,
                                                          AS::wh_consumed,
                                                          AS::wh_regenerated,
                                                          AS::speed,
                                                          AS::current_power_w,
                                                          AS::navigation_active,
                                                          AS::max_speed>();

    auto current_street = m_streets.back();

    auto nav_info = std::format(R"VOBB(nextRd={}
nextRdDesc=
distToNext={} m
totalDist={} m
eta=13:25
ete=5 min
iconHash={:08x}32
        )VOBB",
                                current_street,
                                m_distance_left,
                                5000,
                                kImages[m_current_image].key);

    m_ble_injector.Inject(kChaNav, nav_info);

    if (m_cached_images.find(kImages[m_current_image].key) == m_cached_images.end())
    {
        m_ble_injector.Inject(kChaNavTbtIcon, kImages[m_current_image].data);
        m_cached_images.insert(kImages[m_current_image].key);
    }

    // Always navigating in demo mode
    ps.Set<AS::navigation_active>(true);
    ps.GetWritableReference<AS::distance_traveled>() += distance_traveled_delta;
    ps.GetWritableReference<AS::wh_consumed>() += (2.001 + (m_random_engine() % 5)) * 0.01;
    ps.GetWritableReference<AS::wh_regenerated>() += 0.00101;
    auto& speed = ps.GetWritableReference<AS::speed>();

    const int current_speed = speed;

    // Also simulate the speedometer bottoming out
    constexpr auto kMaxSpeed = 70;
    if (speed != m_target_speed)
    {
        auto sign = speed < m_target_speed ? 1 : -1;
        speed = static_cast<uint8_t>(
            std::clamp(speed + sign * static_cast<uint8_t>(m_random_engine() % 3), 0, kMaxSpeed));

        ps.GetWritableReference<AS::max_speed>() =
            std::max(m_application_state.CheckoutReadonly().Get<AS::max_speed>(), speed);
    }
    else
    {
        m_target_speed = m_random_engine() % kMaxSpeed;
    }
    const int speed_delta = static_cast<int>(speed) - current_speed;
    constexpr int kWattsPerKmh = 10;
    auto target_power = static_cast<int>(speed) * kWattsPerKmh;

    if (speed_delta < 0)
    {
        target_power = -target_power;
    }
    constexpr int kPowerRampStepW = 20;
    auto& current_power = ps.GetWritableReference<AS::current_power_w>();
    int next_power = static_cast<int>(current_power);

    if (next_power < target_power)
    {
        next_power = std::min(next_power + kPowerRampStepW, target_power);
    }
    else if (next_power > target_power)
    {
        next_power = std::max(next_power - kPowerRampStepW, target_power);
    }

    if (m_heading != m_target_heading)
    {
        auto sign = (m_heading < m_target_heading) ? 1 : -1;
        m_heading += sign * std::min(static_cast<uint16_t>(std::abs(m_heading - m_target_heading)),
                                     static_cast<uint16_t>(1));
    }


    current_power = static_cast<int16_t>(next_power);

    GpsData mangled;

    mangled.position = OsmPointToWgs84(m_current_point);
    mangled.heading = m_heading;
    mangled.speed = speed;

    auto qw = m_application_state
                  .CheckoutQueuedWriter<AS::position, AS::pixel_position, AS::gps_position_valid>();
    qw.Set<AS::position>(mangled);
    qw.Set<AS::pixel_position>(m_current_point);
    qw.Set<AS::gps_position_valid>(true);

    return 50ms +
           milliseconds(150 - static_cast<uint32_t>(speed / static_cast<float>(kMaxSpeed) * 150));
}
